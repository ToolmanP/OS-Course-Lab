extern void ch340_write(char buffer[], unsigned size);
extern void go_toy_car(void);
void carboard_controller(char cmd)
{
	printf("cmd is %c, sizeof(forward) is %d\n", cmd, sizeof(forward));

	switch (cmd) {
	case 'w':
		ch340_write(forward, sizeof(forward));
		break;
	case 's':
		ch340_write(back, sizeof(back));
		break;
	case 'a':
		ch340_write(turn_left, sizeof(turn_left));
		break;
	case 'd':
		ch340_write(turn_right, sizeof(turn_right));
		break;
	default:
		break;
	}
}

